const express = require('express');
const cors = require('cors');
const cookieParser = require('cookie-parser');
const dotenv = require('dotenv');
const connectDB = require('./config/db');

// Load environment variables
dotenv.config();

// Connect to database
connectDB();

const app = express();

// Middleware
app.use(cors());
app.use(express.json());
app.use(cookieParser());

// Routes
app.use('/api/auth', require('./routes/authRoutes'));
app.use('/api/users', require('./routes/userRoutes'));
app.use('/api/students', require('./routes/studentRoutes'));
app.use('/api/grades', require('./routes/gradeRoutes'));
app.use('/api/clearance', require('./routes/clearanceRoutes'));

// Admin-specific routes
app.use('/api/admin/mis', require('./routes/misAdminRoutes'));
app.use('/api/admin/registrar', require('./routes/registrarAdminRoutes'));
app.use('/api/admin/accounting', require('./routes/accountingAdminRoutes'));

// Error handling middleware
app.use((err, req, res, next) => {
    console.error(err.stack);
    res.status(500).json({ message: 'Something went wrong!' });
});

const PORT = process.env.PORT || 5000;

app.listen(PORT, () => {
    console.log(`Server running on port ${PORT}`);
});
